        //////////////////////// imple ////////////////////////

        // ì§„ì… ì°¨ìˆ˜ 0ì¸ ì •ì ì„ íì— ë„£ê³  ì‹œì‘í•œë‹¤.
        Queue<Integer> q = new LinkedList<>();
